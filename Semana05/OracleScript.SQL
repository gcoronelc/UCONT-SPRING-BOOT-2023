
CREATE SEQUENCE SEQ_ORGANIZACION START WITH 101 INCREMENT BY 1;
CREATE SEQUENCE SEQ_ESTUDIANTE START WITH 401 INCREMENT BY 1;
CREATE SEQUENCE SEQ_PROYECTO START WITH 701 INCREMENT BY 1;



CREATE TABLE ORGANIZACION
(
	ORG_ID               INTEGER,
	ORG_NOMBRE           VARCHAR2(50) NULL ,
CONSTRAINT  XPKORGANIZACION PRIMARY KEY (ORG_ID)
);



CREATE TABLE ESTUDIANTE
(
	EST_ID               INTEGER,
	EST_NOMBRE           VARCHAR2(50) NULL ,
CONSTRAINT  XPKESTUDIANTE PRIMARY KEY (EST_ID)
);



CREATE TABLE PROYECTO
(
	PRO_ID               INTEGER,
	PRO_NOMBRE           VARCHAR2(50) NOT NULL ,
	ORG_ID               INTEGER NOT NULL ,
	EST_ID               INTEGER NOT NULL ,
CONSTRAINT  XPKPROYECTO PRIMARY KEY (PRO_ID),
CONSTRAINT FK_PROYECTO_ORGANIZACION FOREIGN KEY (ORG_ID) REFERENCES ORGANIZACION (ORG_ID),
CONSTRAINT FK_PROYECTO_ESTUDIANTE FOREIGN KEY (EST_ID) REFERENCES ESTUDIANTE (EST_ID)
);